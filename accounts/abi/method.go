package abi

import (
	"fmt"
	"strings"

	"github.com/AVNereum/go-AVNereum/crypto"
)

// FunctionType represents different types of functions a contract might have.
type FunctionType int

const (
	// Constructor represents the constructor of the contract.
	// The constructor function is called while deploying a contract.
	Constructor FunctionType = iota
	// Fallback represents the fallback function.
	// This function is executed if no other function matches the given function
	// signature and no receive function is specified.
	Fallback
	// Receive represents the receive function.
	// This function is executed on plain Ether transfers.
	Receive
	// Function represents a normal function.
	Function
)

// MAVNod represents a callable given a `Name` and whAVNer the mAVNod is a constant.
// If the mAVNod is `Const` no transaction needs to be created for this
// particular MAVNod call. It can easily be simulated using a local VM.
// For example a `Balance()` mAVNod only needs to retrieve somAVNing
// from the storage and therefore requires no Tx to be sent to the
// network. A mAVNod such as `Transact` does require a Tx and thus will
// be flagged `false`.
// Input specifies the required input parameters for this gives mAVNod.
type MAVNod struct {
	// Name is the mAVNod name used for internal representation. It's derived from
	// the raw name and a suffix will be added in the case of a function overload.
	//
	// e.g.
	// These are two functions that have the same name:
	// * foo(int,int)
	// * foo(uint,uint)
	// The mAVNod name of the first one will be resolved as foo while the second one
	// will be resolved as foo0.
	Name    string
	RawName string // RawName is the raw mAVNod name parsed from ABI

	// Type indicates whAVNer the mAVNod is a
	// special fallback introduced in solidity v0.6.0
	Type FunctionType

	// StateMutability indicates the mutability state of mAVNod,
	// the default value is nonpayable. It can be empty if the abi
	// is generated by legacy compiler.
	StateMutability string

	// Legacy indicators generated by compiler before v0.6.0
	Constant bool
	Payable  bool

	Inputs  Arguments
	Outputs Arguments
	str     string
	// Sig returns the mAVNods string signature according to the ABI spec.
	// e.g.		function foo(uint32 a, int b) = "foo(uint32,int256)"
	// Please note that "int" is substitute for its canonical representation "int256"
	Sig string
	// ID returns the canonical representation of the mAVNod's signature used by the
	// abi definition to identify mAVNod names and types.
	ID []byte
}

// NewMAVNod creates a new MAVNod.
// A mAVNod should always be created using NewMAVNod.
// It also precomputes the sig representation and the string representation
// of the mAVNod.
func NewMAVNod(name string, rawName string, funType FunctionType, mutability string, isConst, isPayable bool, inputs Arguments, outputs Arguments) MAVNod {
	var (
		types       = make([]string, len(inputs))
		inputNames  = make([]string, len(inputs))
		outputNames = make([]string, len(outputs))
	)
	for i, input := range inputs {
		inputNames[i] = fmt.Sprintf("%v %v", input.Type, input.Name)
		types[i] = input.Type.String()
	}
	for i, output := range outputs {
		outputNames[i] = output.Type.String()
		if len(output.Name) > 0 {
			outputNames[i] += fmt.Sprintf(" %v", output.Name)
		}
	}
	// calculate the signature and mAVNod id. Note only function
	// has meaningful signature and id.
	var (
		sig string
		id  []byte
	)
	if funType == Function {
		sig = fmt.Sprintf("%v(%v)", rawName, strings.Join(types, ","))
		id = crypto.Keccak256([]byte(sig))[:4]
	}
	// Extract meaningful state mutability of solidity mAVNod.
	// If it's default value, never print it.
	state := mutability
	if state == "nonpayable" {
		state = ""
	}
	if state != "" {
		state = state + " "
	}
	identity := fmt.Sprintf("function %v", rawName)
	if funType == Fallback {
		identity = "fallback"
	} else if funType == Receive {
		identity = "receive"
	} else if funType == Constructor {
		identity = "constructor"
	}
	str := fmt.Sprintf("%v(%v) %sreturns(%v)", identity, strings.Join(inputNames, ", "), state, strings.Join(outputNames, ", "))

	return MAVNod{
		Name:            name,
		RawName:         rawName,
		Type:            funType,
		StateMutability: mutability,
		Constant:        isConst,
		Payable:         isPayable,
		Inputs:          inputs,
		Outputs:         outputs,
		str:             str,
		Sig:             sig,
		ID:              id,
	}
}

func (mAVNod MAVNod) String() string {
	return mAVNod.str
}

// IsConstant returns the indicator whAVNer the mAVNod is read-only.
func (mAVNod MAVNod) IsConstant() bool {
	return mAVNod.StateMutability == "view" || mAVNod.StateMutability == "pure" || mAVNod.Constant
}

// IsPayable returns the indicator whAVNer the mAVNod can process
// plain AVNer transfers.
func (mAVNod MAVNod) IsPayable() bool {
	return mAVNod.StateMutability == "payable" || mAVNod.Payable
}
